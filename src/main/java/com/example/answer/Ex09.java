package com.example.answer;

public class Ex09 {

	public static void main(String[] args) {

		int num1 = 30;
		int num2 = 5;

		System.out.println(num1 + " + " + num2 + " = " + (num1 + num2)); // 足し算の計算を先にやるので()で囲む必要があります
		System.out.println(num1 + " - " + num2 + " = " + (num1 - num2)); // 引き算の計算を先にやるので()で囲む必要があります
		System.out.println(num1 + "️ * " + num2 + " = " + num1 * num2);
		System.out.println(num1 + " / " + num2 + " = " + num1 / num2);

	}

}
